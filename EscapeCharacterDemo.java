package com.syntexpro2.basic;

public class EscapeCharacterDemo {
    public static void main(String[] args) {

        System.out.println("shanta \nbhuiya");
        System.out.println("shanta \"bhuiya\"");
        System.out.println("shanta \t\t\tbhuiya");
        System.out.println("shantaa\b bhuiya");
        System.out.println("shanta \rbhuiya");
        System.out.println("shanta \\bhuiya/");

    }
}
